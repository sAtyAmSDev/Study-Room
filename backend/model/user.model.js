import mongoose from "mongoose";

const userSechema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export const Users = mongoose.model("Users", userSechema);
